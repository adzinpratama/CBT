    private void datatable() {
        //To change body of generated methods, choose Tools | Templates.
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("Id Ujian");
        tbl.addColumn("judul");
        tbl.addColumn("nama mapel");
        tbl.addColumn("tanggal");
        tbl.addColumn("waktu(menit)");
        tbl.addColumn("Jumlah soal");
        table.setModel(tbl);
        try{
            Statement statement = (Statement)conek.GetConnection().createStatement();
            ResultSet res=statement.executeQuery("select * from ujian where id_user = '"+id_user+"'");
            
            while(res.next()){
                tbl.addRow(new Object[]{
                    res.getString("id_ujian"),
                    res.getString("judul"),
                    res.getString("nama_mapel"),
                    res.getString("tanggal"),
                    res.getString("waktu"),
                    res.getString("jml_soal"),
                   
                        
                });
                table.setModel(tbl);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(rootPane, "Error");
        }
    }
